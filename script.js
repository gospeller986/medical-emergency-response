async function addHospitalToDatabase(hospital) {
    try {
        console.log(JSON.stringify(hospital));
        const response = await fetch('http://localhost:8080/api/v1/hospital/add', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify(hospital)
        });

        if (response.ok) {
            console.log(`Hospital '${hospital.name}' added to the database.`);
        } else {
            console.error(`Failed to add hospital '${hospital.name}' to the database. Status: ${response.status}`);
        }
    } catch (error) {
        console.error(`Error adding hospital '${hospital.name}' to the database:`, error);
    }
}

async function addHospitalsToDatabase(hospitals) {
    for (let hospital of hospitals) {
        await addHospitalToDatabase(hospital);
    }
}

